                            	<p align="justify" class="question"><strong><u>Thah (1974)</u></strong></p>
                        
                        		<p align="justify">The canvas depicts images of two women in bold contrast. In reality the painting reflects on two different societies that perceive a woman in their two respective ways.</p>
                        	</div>
                            <div class="col-lg-6 col-md-6 col-sm-12">
