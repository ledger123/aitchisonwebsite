            <p>Prep sessions in the evening are supported by residential teaching staff. This means boarders have access to specialist subject help throughout the school week. There is no need for external tutors. Our boarders regularly top academic results. Senior boys have access to the internet for study during most of the day and evening. There are strict rules on internet usage, which is monitored. Each house has PCs and a printer to assist with studies. Mobile phones are permitted for seniors between certain hours. <br></p>
            <p>Boarding at Aitchison develops each boy’s independence and creates a network of close friends in a secure, safe and positive environment. The College regularly supports the development of boys with helpful contributions from senior staff, visiting experts, and the College doctor. The health and well-being or our boarders is top priority. Boys attend prayers at the College mosque daily. <br></p>
