                            <p class="question">Did you always have a rebellious streak in you?</p>
                            <p align="justify">Principal Abdul Ali Khan used to call me &lsquo;a rebel without a cause&rsquo;. The reason was that there were some issues that I took up with the Principal in the school assembly. The principal had stopped the cars from coming in through the main gate to drop the boys. But the Governor’s car bringing his grandsons was allowed inside and I protested. Although I used to go on a bicycle to the school, I said that this was very unfair and there should be no special privilege. </p>
                            <p align="justify">There was a time when Mr. Abdul Ali Khan was very rough with an employee of the College who looked after our bicycles. One day I found him about to physically hit this man. The Principal was very angry. As a young student I stood in his way and said, &ldquo;Sir, no please, no please, no.&rdquo; So he was very angry with me. He used to call me a rebel without a cause.</p>
                            <p class="question">You’ve always been resilient in the face of adversity. What keeps you motivated?</p>
                            <p align="justify">My late parents and my grandfather, Chaudhary Bahawal Baksh. My grandfather was a man who resisted and raised his voice even under the British Raj. In 1946, my grandfather who was 81 years old courted arrest. Three days later, my father resigned from government and courted arrest and 3 days after that my mother carrying me in her arms as a one year old, courted arrest. So there was a political tradition of resistance in the family. In the 1960&rsquo;s my father had to suffer because of the resistance to Ayub Khan&rsquo;s military rule. In the 80&rsquo;s, I was resisting Zia-ul-Haq&rsquo;s rule and I was jailed. Against Musharraf, I led the Lawyers&rsquo; Movement and I was put in jail again. So there is a history. </p>
