                                <br/>
                                <p align="justify" class="question"><strong><u>Tree Bouquet (1981)</u></strong></p>
                        
                        		<p align="justify">The Bouquet was painted to celebrate the first anniversary of Zia’s imposition of martial law. It was fabricated from a barbed wired and placed in a common glass. It was then placed on blood stained table top of an interrogator and displayed under naked glare of an electric light.</p>
                            </div>
                        </div>
                        
                        <br/>
                        <p align="justify" class="question"><strong>What does success mean to you?</strong></p>
                        
                        <p align="justify">Success is quite a trite word. It’s like you’ve crossed a certain line. I think life is to be lived to the very end and beyond. Success seems a line you have crossed, where things come to an end. I would say that I’ve had a fulfilling life in the sense that I have good friends, I get up every day with a clear conscience and there is always something on my mind which needs to be done. It could be anything that can make my life greener, makes it worth living and gives me satisfaction, propel growth and evolution of ideas.  Through my work I reach out to other people and make a bonding with them. I think that is a better way to describe my success. I am successively successful when I feel that I have done something worthwhile. It is a continuous process.</p>
                        
                        <br/>
                        <p align="justify" class="question"><strong>Any advice for our present students?</strong></p>
                        
                        <p align="justify">I think it is not enough to be just good and tolerant. An Aitchisonian must simultaneously condemn intolerance that is evidently false and bad. It is important for an Aitchisonian to be a proactive person than just be a reactive Aitchisonian.</p>
                        
                        <p align="justify">An Aitchisonian should be role model for society for never lying. He should be a proud Aitchisonian but a modest one. Now I am lecturing. An Aitchisonian should not lecture, but they love it; must have the last word.</p>
                        
                        
                        
