                        <p align="justify" class="question"><strong><i>Mr. Iftikhar Ahmad Malik, former Vice Principal (Admin)/Bursar and an Old Aitchisonian sadly passed away on March 13th, 2017.</i></strong></p>
                        
                        <p align="justify">Iftikhar Ahmad Malik hailed from a small village in the district Gujarat. He received his early education at Bishop Cotton School in Simla (India) before moving to Pakistan in 1947. In Pakistan, he first studied at St. Anthony's High School, Lahore and later joined Aitchison College.</p>
                        
                        <p align="justify">He was admitted to the College as a day boy in 1954 and was transferred to Godley House soon after. In 1956, he was awarded the House colour and the Godley House Cup for the best all-round leaving boy. He left the College on 31st December, 1956 after his Higher School Certificate (HSC) Examination.</p>
                        
                        <p align="justify">In 1958, he graduated from Government College and thereupon joined Pakistan Shell Oil Company in Karachi. A few years later in 1962, he started his job at Pakistan Tobacco Company, where he achieved remarkable career progression. Upon retirement from the Company, he was honored by the Board for his work and contribution of over 35 years of service.</p>
                        
                        <p align="justify">In 1997, Mr. Iftikhar was invited to join Aitchison College to assist in the financial areas. He wholeheartedly served his old School, first as the Bursar and later as the Vice Principal (Admin)/Bursar.</p>
                        
                        <p align="justify">Having been an Aitchisonian himself, Mr. Iftikhar knew the College inside and out.  He gave everything he had to the College and contributed substantially to its development.</p>
                        
                        <p align="justify">The former Godley-ite was a man of action. “Constant development and life are synonymous,” he would say. He would be excited to embrace innovation and to adopt new ideas, more so than his younger counterparts. He is credited with modernizing the facilities of the College, especially in terms of bringing I.T. facilities up-to-date with modern standards. He represented everything that was old school quality, but had a vision for the future.</p>
                        
