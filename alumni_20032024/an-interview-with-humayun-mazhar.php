                    <div class="blog-news-details blog-single-details">
                      <p align="justify" style="font-style: italic;">Humayun Mazhar, the proud orchestrator of Pakistan’s premier software export company is also a proud Aitchisonian. Humayun set up CresSoft in the 90’s. A company that put the country on the global software sourcing map, it made the IT graduate the most eligible bachelor in the country. Currently, he wears two hats in the world of entrepreneurship: he’s the founder and CEO of CresVentures- an angel capital firm- and also serves as President of the Indus Entrepreneurs Lahore Chapter. In the words of Steve Jobs, he “stays hungry, stays foolish and follows his heart.” In the following interview, Humayun reminisces about his school days and talks about the role that Aitchison played in his success story.</p>
					<p class="question">Could you please tell something about your time at Aitchison? When did you join and how did you like it here?</p>
                    <p align="justify">My first exposure to Aitchison was through a summer camp run by G.D. Langlands. I spent the summer acquainting myself with Aitchison and joined K-5 in the following fall. I was a boarder through-and-through - Gywn house, Saigol house and then Kelly House. The principal for the college at the time was Chaudhary Ghulam Rasool, and Kelly house was filled mostly with sportsmen. Kelly-ites would win most inter-college games and were best described as ruffians. I played Hockey - I was part of Junior School’s team and then the College team for a while. At Aitchison sports have always been very competitive. So it was no surprise that at the time you had to be extremely good at sports to get into any of the teams. Another passion that I realized during my time at Aitchison was squash.</p>
                    <p  class="question">How do you recall your school days? </p>
                    <p align="justify">Adapting to the environment of the hostel was initially tough; there was a certain degree of decorum expected from everyone. Ms. Gore was at Junior School at that time, and in the hostel you were supposed to write a letter to your parents every week. Since these correspondences were monitored, when I tried to send a letter to my parents asking them bring me back home, I immediately got into trouble. Apart from minor hiccups, I have very fond memories of my time at Aitchison. Junior School was more of an adjustment experience, but after the first year, I got into the groove of Aitchison in Prep School.</p>
                    <p align="justify">I found myself clicking with people who had come from all over the country. It was a very good cultural blend of people from all sorts of backgrounds. And this was at a time when Aitchison was transforming from a very elite school to a school that was also open to the general public.  So it was a big learning experience for me and also for others around me. Aitchison has always been a big brand name; in those days, getting a lift from Mall Road was very easy – if you were wearing an Aitchison badge or a turban, people would go out of their way to give you a lift. I have very fond memories from that time since I spent the better part of my formative years at Aitchison. </p>
                    <p  class="question">Anything that you particularly admire about the College? </p>
                    <p align="justify">One thing Aitchison teaches you is discipline. Also, there is always this sense of belonging. In the boarding house, everyone was equal, regardless of where they came from. We spent ample time together and forged friendships that would last a lifetime. As a result, we identified collectively against other schools, relishing the opportunities that we were given.</p>
                    
                    <p align="justify">I’ve had experiences abroad where if you told someone you were from Aitchison, a lot of doors would open for you. Old Aitchisonians, regardless of what batch they were from, would give you the time of the day. The Aitchison network is truly incredible- very recently I was in India; I was sitting with a friend when I happened to mention Aitchison. Two Old Aitchisonians from the pre-partition days happened to be sitting there. They not only paid for our food but also insisted that I join them for dinner. </p>
                    <p  class="question">You’ve served in various important management and leadership positions. Does Aitchison play any role in your success story?</p>
                    <p align="justify">One of the fundamental things I learnt during my time here was how to interact with people from all avenues of life. Some of my best friends were from KPK, Balochistan and Sindh, so I got exposed to their culture at an early age. As a result I was more receptive to my environment from the get-go. Later, when I started interacting with people from overseas and set up a few joint ventures, this flexibility played a crucial part in opening doors for me. </p>
                    <p align="justify">Another thing that was reinforced throughout was “perseverance commands success”, as clichéd as that might sound. If I look back, my software company for which I later won various accolades didn’t do well from day one. I almost shut it down after four years, and whatever I’d invested was almost depleted, but I didn’t give up that easily. And then lady luck smiled upon me; things just happened. I will attribute that perseverance and competitive spirit to Aitchison.  </p>
                    
                    <p  class="question">You support different causes, particularly education. What is it that motivates you to do that extra bit, especially for education?</p>
                    <p align="justify">When I finished my Bachelor’s I asked my father for permission to go abroad for studies. I did my Masters in Business Administration from the US and came back a changed person. I was never a great student and barely got through classes, but at that peak of my life I truly learned. When I came back, I thought about how an average student like myself had done well abroad - an opportunity that was not available to many people. This sticks to me even now. </p>
                    <p align="justify">The most you can give your kids is either a good value system or quality education. These rough diamonds are sprinkled across Pakistan. We need to provide them the right opportunities. The youth in Pakistan, based on my experience in IT industry and elsewhere, given the opportunity they can stand tall with the rest of the world. </p>
                    <p align="justify">Whenever you look at Pakistan and single out the problems that plague it, one of the issues that inevitably comes up is education. My elders started the Crescent Model School in the 60’s, along with the Crescent Foundation. That was a definite factor that affected my attitude towards education; part of it was also from the realization that education or literacy could change everything for the masses. Having said that, I proudly support organizations like CARE and The Citizen’s Foundation.</p>
                    
                    <p  class="question">What does success mean to you?</p>
                    <p align="justify">For me success is a state of mind. Whenever I look back and think that I’ve “been there and done that”, I can safely call that feeling ‘success’. </p>
                    <p align="justify">I have three parameters in my mind to quantify my success: foremost is my family, my kids - that I give them a good value system and quality education. Secondly, the businesses I run; for most of the businesses that I directly manage, one of my goals is to be in the top three industry leaders. I also try and look at the potential for social impact that something I undertake has. Success is a subjective word. What you may describe as success may not float my boat- only my own definition of success can fulfill my aspirations. The proudest I feel is when I look back at what we did with our small company, CresSoft, and the impact that it had on the country as a whole.  </p>
                    
                    
                    
