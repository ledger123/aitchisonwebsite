            <p>Aitchisonian great Syed Babar Ali was Chief Guest at the reopening of the College's iconic building, joined by Mrs Babar Ali and their son, Haider Ali, and watched by two-hundred donors and dignitaries. The Principal Mr Thomson welcomed guests to the special occasion, praising old boy architect Faheem Ahsan Jamil and his team who over a two year period have returned the building to its 1886 perfect condition. Mr. Thomson thanked donors without whom the restoration could not have been accomplished. He added comments regarding the building's planned use for A level boys, and how each boy would gain genuine appreciation of the school's history. Classrooms are adorned with honour boards. He explained the layout of the restored building and especially the 'Aitchison Club' lounge room for seniors adjoining a new university guidance office. Mr Thomson paid tribute to Syed Babar Ali, mentioning that he was the perfect person to preside over the occasion. Faheem Ahsan Jamil showed slides of the restoration and explained how intricate to substantial works were carried out. Syed Babar Ali offered a reflection and congratulated Faheem on his remarkable achievement, while thanking the Principal for his initiative. An afternoon Tea was held in the Hall of Honour, followed by a guided tour with College prefects.</p>
