                            <p>With friendships forged since senior eleven</p>
                            
                            <p>&#39;Tho&#39; many from a much earlier era too</p>
                            
                            <p>Spawned circa kinders one and two</p>
                            
                            <p>While some of our brothers were boarders</p>
                            
                            <p>Who caught on quick not to be hoarders</p>
                            
                            <p>Teaching us day boys too how to share</p>
                            
                            <p>By instilling that rare quality to care</p>
                            
                            <p>These were some life lessons learnt</p>
                            
                            <p>And our spurs with a little effort earnt</p>
                            
                            <p>Yes, in this instance boys too were teachers</p>
                            
                            <p>Although no different to us, callow creatures</p>
                            
                            <p>A fine family over several seasons thus made</p>
                            
                            <p>Indeed a great gift by God&#39;s grace for each grade</p>
                            
                            <p>Endowed with mind and muscle in equal measure</p>
                            
                            <p>To be our treasure in both labour and leisure</p>
                            
                            <p>&lrm;We had champions we could pitch on the playing field</p>
                            
                            <p>And with SC 6 and 7s, cerebral victories we sealed</p>
                            
                            <p>&lrm;Taught by titans once we tirelessly were</p>
                            
                            <p>Who can still set our hearts astir</p>
                            
                            <p>Even without them anymore at the helm</p>
                            
                            <p>&lrm;Their reign is undimmed over our inner realm</p>
                            
                            <p>&lrm;Pray, tell, where would we, blessed boys, be?</p>
                            
                            <p>Sans our Sirs, would our spirits still soar free?</p>
                            
                            <p>Abetting ethical and intellectual inquiry</p>
                            
                            <p>Backed by firm faith and a tempo fiery</p>
                            
                            <p>Like working with putty on unfinished souls</p>
                            
                            <p>&lrm;Priming personalities and refining our roles</p>
                            
                            <p>With pain and passion they us groomed</p>
                            
                            <p>To face challenges that ominously loomed</p>
                            
                            <p>Yes, they masterfully minted men out of us</p>
                            
                            <p>Now we know for what was all that fuss</p>
                            
                            <p>Both in the classroom and on the green</p>
                            
                            <p>To play the game with a camaraderie keen</p>
                            
                            <p>Shaped by chivalry not rivalry in all ventures</p>
                            
                            <p>By shunning the lure of moral misadventures</p>
                            
                            <p>Cultivating character above all</p>
                            
                            <p>To stand tall each time we fall</p>
                            
                            <p>Madams of junior school did their duty too</p>
                            
                            <p>From whom we received rules of etiquette true</p>
                            
                            <p>&lrm;We were mentored to make our mark on this world</p>
                            
                            <p>While flying the flag that our elders unfurled</p>
                            
                            <p>&lrm;Over the years our ranks have thinned</p>
                            
                            <p>Counting comrades at whom the grim reaper has grinned</p>
                            
                            <p>For with sorrow we see some are no more</p>
                            
                            <p>Yet by the selfsame we still set store</p>
                            
                            <p>&lrm;And even if our numbers are by death depleted</p>
                            
                            <p>We won&#39;t let their vital voices be defeated</p>
                            
                            <p>Honouring hence our dear companions departed</p>
                            
                            <p>As if we never forever from them parted</p>
                            
                            <p>So to them we say, &quot;Tarry awhile until we again meet&lrm;&quot;</p>
                            
                            <p>But, alas, not before our task here is complete</p>
                            
                            <p>&lrm;And whether or not we all walk into heaven</p>
                            
                            <p>We&#39;ll always be that class of seventy seven</p>
    
                            
