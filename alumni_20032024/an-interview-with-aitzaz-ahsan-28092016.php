                        	<p align="justify" style="font-style: italic;">
                            	Who would have known 60 years ago that Aitchison&rsquo;s &ldquo;rebel without a cause&rdquo; would one day become Pakistan&rsquo;s renowned political figure and a rebel with a cause to uphold democratic values. Leader of the Opposition in the Senate, Chaudhry Aitzaz Ahsan has been fighting for his cause on various fronts. He is an eminent Barrister, former President Supreme Court Bar Association, former Federal Minister, a writer and an intellectual. He is widely lauded for his leading role in the 2007-2009 Lawyers&rsquo; Movement for the restoration of an independent judiciary. Read on to find out more about Aitzaz Ahsan&rsquo;s school days, his most prized achievement and source of motivation.
                            </p>
                            <p class="question">When did you join Aitchison College and what was it like back then?</p>
                            <p align="justify">I joined Aitchison in 1954. There were less than 300 students in the school altogether. One therefore knew most of the contemporaries not only in one’s own class but in classes above and below. There was a greater emphasis then on sports and relatively less on academics, so most of the outstanding students excelled in the sports field. The standard of academic excellence, so to say, was not as high as it is today. </p>
                            <p align="justify">The Principal was Syed Zulfiqar Ali Shah most of the time. Then in 1962 he retired and was replaced by Mr. Abdul Ali Khan. Zulfiqar Ali Shah was a very lax and relaxed gentleman. Then suddenly Abdul Ali Khan, a very strict disciplinarian took over and that was a shock for us.</p>
                            <p class="question">Any fond memories from your school days?</p>
                            <p align="justify">Yes! At Aitchison, we got introduced into a plural environment. We had with us some international students also. The faculty itself was plural. In the Junior School, in my time, we had some lovely teachers - Ms. Gore was the Headmistress, Ms. Bholanath was a Hindu teacher, Ms. Morris was a Christian teacher, Ms. Bukhari was a Muslim teacher.</p>
                            <p align="justify">The faculty in the Prep and Senior Schools included some very devoted teachers like Mr. Tajuddin, Mr. Fedden, Mr. T. L. G. Malan. Mr. Goldstein, the Headmaster of the Senior School, taught us nothing but etiquette, manners and character building. There were also female teachers; Mrs. Neilson and Mrs. Feddon. They were quite a devoted lot of teachers. </p>
                            <p class="question">Did you win any awards at Aitchison College?</p>
                            <p align="justify">I received the Godley Medal which was given to the best student in English Literature and Language. I also won the Chiragh Khan Debating Cup. </p>
                            <p class="question">What kind of impact did Aitchison have on your life? </p>
                            <p align="justify">The environment at Aitchison provided me with an ideal foundation for studying abroad at Cambridge.  However, the environment was not suited for living in Pakistan.</p>
                            <p class="question">Which one achievement are you most proud of?</p>
                            <div class="row">
                              <div class="col-sm-5" align="justify">
                              <p align="justify">The second of December, 1988 – Benazir Bhutto Shaheed had just taken oath as Prime Minister and I was announcing the release of around 8000 political prisoners on her behalf as the Law Minister and Interior Minister. 
These were all prisoners of a brutal martial law period of Zia-ul-Haq, when people were flogged and hanged by orders of military courts which gave no justice. They were all basically arrested for political activity against Zia’s regime or for speaking or writing against it.  It opened the gates of all the prisons for political prisoners.</p>
                              </div>
                              <div class="col-sm-7">
