           <!--<div align="center"><strong>Dropping Boys to School</strong></div><br>-->
           <p>To be a leader in education requires  regular self-analysis of how teaching, learning and assessment take place.  Three vital decisions in 2016 will set us on the right path:</p>
<ol>
  <li><strong>Aitchison  becomes a Cambridge School</strong></li>
             <li><strong>Aitchison  adopts the Harkness Table</strong></li>
             <li><strong>Aitchison  creates an Adjunct Faculty</strong></li>
         </ol>
           <p>By making the decision to become a  Cambridge school, we are acknowledging that teaching, learning and assessment  requires an integrated framework of intellectual &lsquo;stepping stones&rsquo; from Grade 2  to AS/A levels. <strong>This progression or  curriculum scaffolding cannot be left to chance. </strong>Why? Because teaching,  learning and diagnostic assessment are best managed through a combination of  summative and formative assessments in the preparation years. </p>
<p>When we used to be told that a boy  achieved a 75% in an English or Mathematics examination, what did this really  mean? In effect, it was little more than a global assessment (on a particular  day) that suggested that a boy had not mastered 25% of his course. But how  diagnostic was this summative measurement? How was it employed to guide  improvement? The response to both these questions would disappoint most  educators, parents and learners. Usually, when a student gets something wrong,  it is traceable to particular deficiencies in skills and a particular application  of knowledge. Examinations are good for ranking, but they are not so effective  in identifying.  </p>
<p>Only regular formative assessment  can achieve this in the broader compass of assessment styles. Examinations will  always be crucial, but by working in strands of learning and schemes of work,  it allows teachers to pinpoint strengths and &lsquo;unformed&rsquo; capabilities through  targeted outcomes. It will also help teachers focus on specific learning needs  and report on them through sophisticated data analysis. There is no sense of  &lsquo;flying blind&rsquo; here or generalizing strengths and weaknesses from one year to  the next. Future diagnostic checkpoints at the end of Primary and Secondary 1  are one part of this approach. </p>
<p>Across many years of developing and  leading international schools, I have found no better system than what  Cambridge University has assembled. It is, after all, the world&rsquo;s largest  curriculum body. Inquiry based classrooms help teachers create interesting classroom  experiences and build greater clarity in the outcomes of teaching, learning and  assessment. Boys begin to learn the value of discussion and hopefully the joy  of creativity. Traditional classes concentrate on reading and writing (and  memorizing). As important as these skills remain, we must add speaking and  listening to complete the educational experience and satisfy taxonomy.  &lsquo;Mountains&rsquo; of homework and aimless content do not achieve this.</p>
<p style="text-align:center">&nbsp;<img src="images/18sep2016.png" width="400" height="276" /></p></div>
           <p>In 2018, we will offer the  International GCSE or IGCSE, and in 2020 we will introduce International AS and  A Levels. Superior subject choices are one advantage, and so are specific  offerings/choices for Pakistan students in Pakistan Studies, Islamyiat, Urdu  and Arabic. Importantly, each Cambridge program will be stepped seamlessly from  one school to the next and the styles of learning will be consistent. Taking  boys on a journey from primary to senior expectations will be easier and more  logical. It will bring teachers from the three schools into a greater sense of  teamwork and co-responsibility. The style of learning also makes this the  perfect framework for university entrance –and life.</p>
<p style="text-align: center;"> ***</p>
           <p><strong>However, a new curriculum is not in itself sufficient.</strong> Aitchison  is eager to find ways in which boys can discuss more about what they have  learned or do not understand or would like to imagine. While the Harkness Table  was founded in the 1930s, its value remains forceful. Discussed around a table,  boys can prove that education is not a process of telling and being told. It is  an active and constructive process of which they have control and benefit. Not  only does this enable boys to &lsquo;open up&rsquo; (which they are notoriously poor at  doing), they can help one another. Older parents might know this as a Socratic  exercise. The Harkness Table will be gradually introduced into Middle School  and Senior School classes as either part of lessons or discreet exercises on a  regular basis. <strong>Aitchison wants  student-centred, discussion based, intellectually rigorous and emotionally  honest classes.</strong></p>
<p style="text-align: center;"> ***</p>
           <p>Linking schools to talented people  beyond school is the act of all great schools. <strong>The Aitchison Adjunct Faculty</strong> will &lsquo;value-add&rsquo; classes and  learning; taking our most senior boys out of their comfort zone or linking  learning to university insights and even practical/professional life itself.  This will be initially conducted in Art and Design, English &amp; Literature,  Mathematics, Science, Law, Economics and Business Studies.</p>
<p style="text-align: center;"> ***</p>
           <p>Through these first three  initiatives, Aitchison aims to lift itself into the status of a &lsquo;reflective  school&rsquo;, committed to boys not merely successfully negotiating examinations  (which should be taken for granted), but graduating as interesting, informed  and creative young men. </p>
