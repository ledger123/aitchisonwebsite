            <h2 class="titile">Benefits of Boarding </h2>
            <p>Boarders have access to a glorious campus that offers a wide range of educational, sporting facilities and peaceful outdoor settings. Opportunities abound: cricket, football, hockey, basketball, swimming, tennis, squash and horse riding. In pursuing healthy interests a boy works and plays as part of a team, making life-long friendships and gaining a true sense of what it is to be an Aitchisonian. The campus has many beautiful outdoor locations including the forecourt of houses where boys gather to chat or read a book. <br></p>
            <p>Formal breakfast, lunch and dinner are taken in the Mess at set times, as are more informal afternoon tea and supper. Weekends are busy on Saturdays with sport and a little more relaxed on Sundays. The College has its own canteens and the famous ‘Deans’ where hot dogs and fruit cups are always favourites after school and on weekends. The College has its own general store, hairdresser, tailor, mail facility, and ATM.<br></p>
