                      <h2><a href="annual_dinner2016.php"><span style="color: #000000;">An Interview With Humayun Mazhar</span></a></h2>
                      <!--<p>Proud Orchestrator of Pakistan’s Premier Software Export Company</p>-->
                      <p><!--Time:<a class="blog-author" href="#"> 4 p.m.</a>--> <span class="blog-date">|11 June 2016</span></p>
