                      <h2>AN INTERVIEW WITH MIAN IJAZ UL HASSAN</h2>
                      <!--<p>Alumni enjoy grand musical evening at Aitchison</p>-->
                      <!--<p><span class="blog-date">02 December 2016</span></p>-->
                    </div>
                    <div class="blog-news-details blog-single-details">
                        
                        <div align="center">
