                        <p align="justify" class="question"><strong><i>Old Aitchisonian Mian Ijaz ul Hassan is one of Pakistan’s most celebrated artists. He is a prolific painter, as well as a writer and a leading art historian. He is a recipient of President’s Award for Pride of Performance and Sitra-e-Imtiaz. His art has not only received national acclaim but has been featured in exhibitions around the world.</i></strong></p>
                        
                        <p align="justify" class="question"><strong><i>In his interview, Ijaz shares some fond memories of the school that became a home, the housemistress who is remembered as a mother and the boys who became life-long friends.</i></strong></p>
                        
                        <br/>
                        <p align="justify" class="question"><strong>When did you join Aitchison and what was it like back then?</strong></p>
                        
                        
                        <p align="justify">When I joined in March 1948, there were approximately a hundred boys here. When I left after doing my HSC in 1957, the number had increased to over three hundred.  It was a College of the Federation; there were students here who were Sindhi, Baloch, Pathan and Punjabi and we all knew each other. No matter where you would have abandoned an Aitchisonian in the country, he would have found bed and breakfast there within an hour.</p>
                        
                        <p align="justify">All our time was spent or rather exhausted here in the school. When we were in senior classes we could slip out by taking an occasional French leave.  Riding on our own or borrowed cycles we would go to McLeod Road to eat takka-tin or watch a film and then rush back. We would frequently go to the morning film shows and regularly get attached to a beautiful face-spending the whole next week romanticizing about it. Subsequently one would fall for another the following week. We could easily indulge in our fantasies because there was no dearth of beautiful faces. Sometimes a teacher noticed that we had been up to some extra-curricular mischief and hinted at but never confronted us. Therefore, out of deference to them we often tried restraining our spontaneous inclinations.</p>
                        
                        <p align="justify">There was camaraderie amongst us. Friendships mattered and counted a lot. We also had a vibrant association with many Lawrence College students. Tragically I lost some dear ones in the 1965 war.</p>
                        
                        <p align="justify"> I believe hobbies are very important in enhancing our manual skills and inventive faculties. There were innumerable pursuits that Aitchisonians were exposed to. We could take up cycle repairing, wood work, weaving, leather work, cane work, music and art as well as gardening.</p>
                        
                        <br/>
                        
                        <div class="row">
                        	<div class="col-lg-6 col-md-6 col-sm-12">
                            	<p align="justify" class="question"><strong>Were you in any of the sports teams?</strong></p>
                        
                        		<p align="justify">Yes I was in almost all the teams. I was awarded the Full College Sports Blazer and won the Aitchison Challenge Cup in 1957.  I was captain of the athletics team and the football team. I was also in the hockey team. Mr. Ghulam Rasool, the olympic hockey player, was our coach. I was also in the Cricket Team, which meant that our Sunday was used up playing cricket matches against local cricket clubs.</p>
                        	</div>
                            <div class="col-lg-6 col-md-6 col-sm-12">
                                <img src="img/alumnusinfocus/ijazulhassan/2.jpg" class="img-responsive" />
                            </div>
                        </div>
                        
                        
                        
                        <p align="justify" class="question"><strong>How was your experience as a boarder?</strong></p>
                        
                        <p align="justify">In the beginning it was traumatic when I was packed off to L J House. I tried running away thrice but was caught and brought back. After a while I gave up and slowly settled down. When I look back, even now I find the initial years grievously disturbing. Actually, during that time I felt totally lost. I came from a home where I spoke Punjabi with my mother. At Aitchison people did not speak my language I was abruptly initiated into English and forbidden to speak in Punjabi; anyone caught speaking it, was given a dagger. So one of the major inhibiting factors was, not knowing English. I could not speak even a sentence in it; I was good in Maths and art but didn’t even have a smattering of basic English. As a result I soon lagged behind even in other subjects.</p>
                        
                        <p align="justify">Thank God there was art and sports through which I was able to express myself and establish my identity. At Atichison you had to excel in something to matter. If you were good in nothing you at least needed a sense of humour to survive, which at that early stage I had none.</p>
                        
                        <p align="justify">Aitchison soon with time became a home to me. My mother would be riled when I referred to Ms. Singer, our housemistress, as my real mother. Ms Singer taught us all the most basic essential requisites of civilized social conduct, how to eat, how to handle your fork and knife and never letting them strike the plate; never to shovel food in our mouths and always to pick the peas one at a time with fork and so on.</p>
                        
                        <p align="justify">One day during classes I felt unwell and was sent from Barry Block to our dormitory to rest. There I saw her sitting in the common dressing room with a tailor. She was rummaging through our clothes that were going to the laundry, checking for inadvertent tears, broken buttons and ink smears. We never knew all this was done under her supervision, no wonder we never had a button missing.  Our cupboards never had a speck of dust on their top. It was always made sure that our nails were clean and that we had a fresh handkerchief in our pockets when we walked to the morning assembly at the Old Building We were made to practice all small courtesies so that we learnt to practice them in life.</p>
                        
                        <p align="justify">In those days there were only a few day school boys; Jubilee House was established much later.</p>
                        
                        
                        <br/>
                        <div class="row">
                        	<div class="col-lg-6 col-md-6 col-sm-12">
                            	<p align="justify" class="question"><strong>Did you always have a creative streak in you?</strong></p>
                        
                        		<p align="justify">Yes! I think I did. However I had no inkling of it then. At Aitchison, I won the Ranbir Shamshir Jung art prize, probably thrice.  Sardar Muhammad was our art teacher and quite a well-known painter. He was succeeded by Khalid Iqbal and Moyeen Najmi. For me both proved to be great mentors. Khalid and Moyeen are today both regarded as our pre-eminent painters of our country. At Atichison I excelled in painting. To my surprise I obtained the highest marks ever, in Art at Punjab University B.A examinations. I was awarded a Scholarship but I went and did my Masters in English. That was partly for my mother’s concern for my career. I did my M.A in English but then went back to art by joining an art school in London. I had a great father. Imagine in those days a father letting his only child to go and join an art school. But my journey did not stop there. A year later I left the Art School and went to reading English at St. John’s College Cambridge.</p>
                        	</div>
                            <div class="col-lg-6 col-md-6 col-sm-12">
                                <img src="img/alumnusinfocus/ijazulhassan/3.jpg" class="img-responsive" style="max-height: 438px;"/>
                            </div>
                        </div>
                        
                        <br/>
                        <p align="justify" class="question"><strong>Is there anything that distinguishes Aitchison College from other institutions?</strong></p>
                        
                        <p align="justify">I think that Aitchison College was a secular institution. In spite of the fact that we were all generally religious minded our attitude and conduct was secular. Some of us were Shias, I hailed from a Sunni family and there must have been an odd Wahabi too – actually we didn’t really know the differences. There were some Christian foreigners as well. We’d go to India and meet Sikhs and Hindus and befriend them too without the slightest hesitation. There was never a division amongst us on the basis of our faith or tongue. The only real conflict was on the basis of the House to which you belonged.  Were you Godly or Kelly?  We had stronger bonding than differences; there were mutual tiffs of course on the sideline, quickly resolved. I think this was a great habit we imbibed at Aitchison and I hope we don’t ever lose this.</p>
                        
                        <p align="justify">Also, I think Aitchisonians crowd for crowd were more amiable, as compared with boys from most other institutions. They stood up and conducted themselves better collectively. We always went out in our blazers to be identified as Aitchisonians. If I meet someone who was senior to me at school short of clicking the heels I extend all respect he deserved.</p>
                        
                        <br/>
                        <p align="justify" class="question"><strong>Is there anything you think the College should be doing?</strong></p>
                        
                        <p align="justify">First and foremost it should keep up all good tradition that we stood for. Additionally with the number of playing fields we have at Aitchison, I would like to see some athletes, hockey and cricket players representing their country. We should also support the arts and if we can’t produce singers, musicians and painters, we should at least produce some visible patrons of the arts.</p>
                        
                        
                        <br/>
                        <p align="justify" class="question"><strong>What work or accomplishment of yours in the field of art are you most proud of?</strong></p>
                        
                        <p align="justify">I think my work of the late ’60 s and ’70s that enlarged upon every day images, street posters and cinema hoardings partly changed the course of Pakistani painting. I used subjects and images that for our citizen were part of his social and aesthetic experience. I picked up subjects and references he easily understood. Some of these works later became internationally known and reside in museums abroad. These works were prized and appreciated for their social and political content. In the Zia era when I could not express my thoughts and feeling freely I resorted to the use of symbols and metaphors to convey my intentions. Many of those paintings and murals can be accessed at my web site: ijazulhassan.com.</p>
                        
                        <p align="justify">In the pre-Zia reign while I was striving to broaden my audience I was also instrumental in organizing exhibitions at village festivals. It was one an effort to take art into the life of common people. Thus some of the paintings of the period became quite popular.</p>
                        
                        
