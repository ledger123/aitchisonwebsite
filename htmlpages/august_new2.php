<h2 style="text-align: center; font-size: 26px;"><strong> LETTER TO PREP SCHOOL PARENTS</strong></h2>

<!--<p align="justify">Dear Parents,</p>
<p>August 02, 2016 </p>-->
           <p><strong>Dear Parents of Prep School,</strong></p>
           <!--<p><strong>Re: Commitment to Aitchison College</strong></p>-->
           <p> The introduction of Cambridge in <strong>English, Mathematics and Science</strong> has been necessary to modify the style of our teaching and assessment in core subjects. It signals an end to the days of rote learning. The memorizing of knowledge is not learning per se; it is memorizing. While memorizing has its place, boys need to apply concepts in thinking for knowledge to be used powerfully and with relevance –the hub of creativity and life-long learning. Into this, we must also insert discussion and reflection. We have, therefore, adopted a whole-school approach that will prepare boys for senior and university thinking, and life beyond school. They will be stepped logically through the secondary years at the right pace and with the right set of skills. </p>
           <p>I am also particularly eager for boys to enjoy the act of learning, which I feel had fallen under the heavy weight and expectation of final examinations and &lsquo;cramming&rsquo; at both junior and prep levels. In seeking change, we are not interested in reaching the destination the &lsquo;old way&rsquo; or faster than someone else.  <strong>We are interested in the quality of learning.</strong> Parents need to appreciate that boys will be involved in activity based thinking and whether some boys are familiar with the early content is immaterial.  It is the thinking component that is special. Parents should be comforted by the fact that the syllabi are set by the University of Cambridge that has many years of combined expertise in this task. <strong>Indeed, Cambridge is the world&lsquo;s largest and most respected education provider across 160 countries.</strong></p>
           <p><strong>We have tailored the Cambridge levels to the right ages (see below), and will conduct International Checkpoint Examinations from March, 2018.</strong> This will give boys time to settle into a new system and provide us with a number of months in which to prepare boys for the next stages, possibly accelerating learning beyond the required levels for those who are advanced and ambitious. There will still be internal examinations for Prep School, but these will carry less weight and be added to by frequent formative assessments. In the larger scheme of things, our current M1 boys will be the first to enter Cambridge IGCSE in C1 (2018) and they will be extremely well prepared for this. </p>
           <p><strong>Course outlines for Cambridge will be posted on the secure Parent Portal in the coming weeks. In the meantime, please allow your son to engage learning in a new, exciting way and avoid external tutors who are not Cambridge trained and who will most assuredly undermine your son&rsquo;s progress and enjoyment of learning.</strong> As a point of useful reference, I stopped teaching my children at around the age of 12 -and I was a teacher. The learning of independence is also a subject in itself. <strong>The current 2016 batch of M2 boys will continue with preparing for the old O Level in C1 and C2.</strong> 
 </p>
 <p>
 In summary, boys will take the proposed IGCSE in 2018 between the ages of 14 and 16. Cambridge A Levels will then be taken from 16 to 18, but 19 is also perfectly fine. The point here is that developmental time can make a great difference to a boy&rsquo;s academic success and adjustment. Equally, the Aitchison curriculum is much broader and demanding than other schools. Boys entering IGCSE will be able to choose subjects as they do now, but in addition to this there will be increased choices. It may interest parents that Mandarin (Chinese) can also be taken as a Second Language that has acceptance among universities as a pre-requisite subject. The IGCSE will be a major development and parents will be invited to a series of school presentations on its introduction, as there will also be opportunities to discuss Junior and Secondary 1 Cambridge early in the year. 
 </p>
 <p>The pattern or scheme of learning under Cambridge at Aitchison is projected as follows:</p>
 <p>
 	<strong>
 		K2 –E1 Junior Cambridge 2016-17 (established)</strong></p><p>
 	<strong>E2 to M1 Secondary Cambridge 2016-17 (established)</strong></p><p>
 	<strong>M2 secondary Cambridge 2017-18 (to be introduced) </strong></p><p>
 	<strong>C1 –C2 Cambridge IGCSE 2018-19 (to be introduced) </strong></p><p>
 	<strong> Cambridge AS & A Levels or IB Diploma 2019-2020 (to be discussed)</strong>
</p>
	<p>Further queries should be directed to the Heads of relevant schools.</p>
