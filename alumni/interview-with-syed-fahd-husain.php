                        <p align="justify" class="question"><strong><i>Old Aitchisonian Syed Fahd Husain is a renowned news media professional with over 25 years of experience in journalism. Over the years, Fahd has held various important positions in the news media industry. He presently wears two hats - as Editor of the Express Tribune and as Executive Director of Express News Television.</i></strong></p>
                        
                        <p align="justify" class="question"><strong><i>In this interview, Fahd takes a trip down memory lane to talk about his time at Aitchison College; an institution that molded his personality. His love for his alma mater comes through as he recalls the cherished memories of his Aitchison experience.</i></strong></p>
                        
                        <p align="justify" class="question"><strong>When did you join Aitchison and what was your first impression about the College?</strong></p>
                        
                        
                        <p align="justify">I joined Aitchison in 1980 in E2 and left in 1986 - the Centenary year.</p>
                        
                        <p align="justify">I can vividly recall the first time I came to Aitchison College as an eleven year old. I had come to take my admission test which was followed by an interview. I was accepted into the College which was a cause for great celebration in our family as it was not easy for an army officer to send his son to Aitchison.</p>
                        
                        <p align="justify">I was a Boarder in the Badar Wing of Saigol House - the Preparatory School Boarding House. Hence, some of my early memories are those of the early morning runs in the cold winter of Lahore and of going to sleep in a track suit for saving another five minutes in the morning to sleep a bit more. Then of course, making all new friends and coming to terms with the fact that one was living with so many people. You leave home and suddenly you are living with around a hundred and fifty boys. There were many boys who had been there right from K-0, so making friends and then settling down in those early days seemed tough. But fortunately, I came across a good number of boys who were willing to make new friends.</p>
                        
                        <p align="justify" class="question"><strong>How was the boarding experience?</strong></p>
                        
                        <p align="justify">Oh, I could write a book about it. I was a boarder throughout; first Saigol House and then Kelly House in Senior School.</p>
                        
                        <p align="justify">I only have fond memories of boarding, even from those early days at Saigol House. There was a lot of discipline as we were doing one thing after another the entire day. There was constantly something or the other which was pushing us whether it was our classes, games, or the call for tea and food. So time flew by at Aitchison College but it was all a lot of fun.</p>
                        
                        <p align="justify">In Senior School, I felt there was more freedom. There was also this deep cultural divide between the boarders and the day boys that has always been there. One of the things that differentiates the boarders is the bonding among them. Since you are not with your family, all those things that you would have shared with your family, you share with your roommate or your best friend as they are the only family you have. You share your experiences with them and make beautiful memories of doing so many things together for the first time.</p>
                        
                        <p align="justify">I believe that one of the best things that Aitchison has to offer is the boarding house. There had come a time, we've heard, when boarding was sadly deprioritized and the numbers had decreased. But I am extremely happy to see Principal Thomson's huge focus on boarding which I believe is the bedrock of the defining experience of Aitchison College.</p>
                        
                        
                        <p align="justify" class="question"><strong>What was the canteen area like?</strong></p>
                        
                        <p align="justify">The staple menu was the same. The hot dogs or the bun kebabs were always our favourite. The most expensive thing was the fruit cup which is still there on the menu.</p>
                        
                        <p align="justify">An interesting part of the canteen culture was that whenever we had inter-house matches, the winning team and the whole house would come to the canteen after the match and pay for everybody's food. So on every special occasion, whenever we had a cricket, football or hockey match, the winning house would come to the canteen chanting their victory and anything that anybody ordered was on the house. Then of course, one can't fail to mention that mysterious red sauce which was a mixture of who knows what but oh so yum!</p>
                        
                        <p align="justify" class="question"><strong>Any fond or interesting memories you'd like to share from your time at the College?</strong></p>
                        
                        <p align="justify">When we were about to graduate, we thought of doing something special. Aitchisonians used to play all kinds of pranks but we had wanted to do something different. We went to the market and got a huge banner made which said â€˜Thank you Aitchison'. We wrote our names on it and hung it over the main curve of the Barry Block where everybody could see it. Access to the roof used to be blocked. I can't recall whether we actually broke it or had somebody open the door, but we somehow managed to get up to the Barry Block rooftop at night. This was against all the rules and we had a very strict headmaster, Usmani Sahab. So when it dawned in the morning, we weren't sure about what the reaction would be. It was a huge banner with our names were written all over it, so everybody knew it was us. But Usmani Sahab was very kind about it. He specifically mentioned and appreciated our gesture of gratitude in the College assembly. He said that had he been aware of our plan earlier, he himself would have got the door opened for us. That was probably the first time that everybody saw the really soft side of Usmani Sahab. It is a very fond memory that we still cherish.</p>
                        
                        
                        <p align="justify" class="question"><strong>Did you have any favourite teacher?</strong></p>
                        
                        <p align="justify">We had wonderful teachers and principal. Colonel N. D. Hasan, our principal, was an absolutely outstanding man. It was unbelievable how he knew every student by name as there were a thousand students. Every morning, he would visit all the boys who were in the hospital. Also, every day, as we would get ready for the boarders' early morning run, he would already be waiting for us at 5 am.</p>
                        
                        <p align="justify">Unfortunately, Colonel N. D. Hasan passed away while we were still there and Mr. Abdul Rehman Qureshi took over as Principal.</p>
                        
                        <p align="justify">Major Azhar, our housemaster, was a huge inspiration for all us Kelly-ites. We copied him; from the way he spoke to the way he dressed. We learned the art of speech from him. If I look back, I think of Major Azhar as somebody who molded our personalities when we were teenagers and in doing so, he has left an indelible mark on our lives.</p>
                        
                        <p align="justify">Then of course we had many wonderful teachers. Mr. Amir Hussain was an excellent English teacher and his classes were very enjoyable. I will always have immense love and respect for him.</p>
                        
                        <p align="justify" class="question"><strong>What were you like as a student?</strong></p>
                        
                        <p align="justify">I was an all-rounder. I was doing pretty much everything at College. I played all sports at House level. I was highly involved in co-curricular from the very beginning.</p>
                        
                        <p align="justify">I was the College captain for Gymastics and a colour holder. I was also awarded the Maulana Altaf Hussain Hali Challenge Cup for best debater.</p>
                        
                        <p align="justify" class="question"><strong>What kind of impact, if any, has Aitchison had on your personal or professional life?</strong></p>
                        
                        <p align="justify">I think it has had a profound impact. In many ways, my personality was molded at Aitchison. Also, a lot of confidence that I gained at that age was because of my experience as a boarder. It taught me how to live away from family and how to form strong bonds with people. The sense of camaraderie brings a lot of emotional enrichment which we were fortunate enough to experience at that young age. We learned new skills and did things that we never thought we could do - whether it was through competing at different levels or solving problems for others as we were living in a huge community 24/7.</p>
                        
                        <p align="justify">If you meet someone, one can tell whether they are from Aitchison. One can identify the deep impact that this institution has on one's personality, thought process and approach towards life. I hope I do not sound elitist when I say this but there is no other school in Pakistan that has such a profound impact on its students. I have studied at several institutions, including foreign universities, but I don't think any other institution has played such a positively powerful role in my life.</p>
                        
                        <p align="justify" class="question"><strong>Is there anything that distinguishes Aitchison from other schools?</strong></p>
                        
                        <p align="justify">It is the overall experience of being part of an institution which has such a grand legacy. When you become part of the Aitchison family, you become part of its legacy. As a kid you do not realize this, but as you grow older, you realize how important a role that legacy plays in your life. Other than providing a certain engulfing depth to the experience of schooling, it also gives you a lot of pride and that pride is very important because it then becomes part of your identity.</p>
                        
                        <p align="justify">Then, of course, it is the overall experience of being in a school that gives you everything you want. You don't need to go anywhere else; everything you need is available within those four walls.</p>
                        
                        <p align="justify" class="question"><strong>Did you always want to be a journalist?</strong></p>
                        
                        <p align="justify">No, not while I was at Aitchison. It was later after finishing College and returning to Pakistan that I decided to pursue journalism. It was mainly because I loved writing, thanks to Mr. Amir Hussain. I remember how he once read out my essay in class in what was one of the proudest moments of my life. "This is how you write English!" he had said. Encouragement like that gives you so much confidence. Now I am a columnist and it is all linked to that love for writing which was developed at Aitchison College.</p>
                        
                        <p align="justify" class="question"><strong>How important is it to have a degree in journalism?</strong></p>
                        
                        <p align="justify">I think for me it was critical. I did not study journalism at undergraduate level or even immediately after that. It was only after having worked as a journalist for 10 years that I did my MS in Broadcast Journalism from Columbia University. The degree was very helpful for me, but when I go to journalism schools here in Pakistan, I am not much impressed to see what they are teaching. When we hire people here, even if they have studied journalism, we still mostly have to teach them everything from scratch. We have many good schools in Pakistan but so far we don't have a good school for journalism. So I would say that you can join the field after having studied anything, as long as you have good communication skills and sound knowledge, along with a deep curiosity about life.</p>
                        
                        <p align="justify" class="question"><strong>As a journalist, you have worked in various positions. Is there any achievement that you are most proud of or a role that was most challenging for you?</strong></p>
                        
                        <p align="justify">When I started doing a talk show, it was a big challenge but one that I really enjoyed. Then when I took on the task of launching the Express News TV channel, that too was very challenging as I had not done anything like it before. My degree in journalism had been more about reporting and being on camera. The channel has since grown to become one of the top TV channels of Pakistan. I'm very proud of the fact that I have helped in setting up three channels: Express News, Express 24/7 and Capital TV.</p>
                        
                        
                        
                        
                        
                        <p align="justify" class="question"><strong>What does success mean to you?</strong></p>
                        
                        <p align="justify">I think success means two things. First, being able to achieve what one really wants; it could be anything that you want and then manage to get it. I think that is success because it reflects perseverance and your determination to achieve your goals.</p>
                        
                        <p align="justify">Second, success to me also means the kind of impact that one can have on the lives of others. It is important to be doing something which is beyond your personal self.</p>
                        

                        
                        
                        <p align="justify" class="question"><strong>Do you often meet other Aitchisonians?</strong></p>
                        
                        <p align="justify">Very often. We recently had the 30<sup>th</sup> reunion of all boarders from our class. A problem with us Aitchisonians is that we still feel and act like little boys when we get together, so wives and families usually get ignored.</p>
                        
                        <p align="justify">We might have a huge social circle now, but the inner core of our social life will always remain Aitchison College. So whenever we want a real bonding session, it will always be with fellow Aitchisonians. Also, being in Lahore helps since there are so many people around here.</p>

                        
                        
                        <p align="justify" class="question"><strong>Any advice for our present students?</strong></p>
                        
                        <p align="justify">Aiming high is very important. People say that you should know exactly what you want to do. I don't agree. I don't think you have to be clear in terms of what exactly you want, as long as you are clear that you want to do something very big. Clarity can come in much later. Secondly, I think every boy should try and experience every aspect of Aitchison College as it is a very special school. If a boy spends his time at Aitchison doing only one or two things, he's actually wasting that experience. I am not saying that you have to be good at everything. But you need to try everything. Do it all so that later you could say that you squeezed every drop of benefit out of the Aitchison experience.</p>
                        

                        
                        
                        
