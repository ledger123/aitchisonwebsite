                        <img src="img/alumnusinfocus/jazibzahir/1.jpg" class="img-responsive" /><br />
                        
                        <p align="justify"><strong><em>Old Boy Jazib Zahir is an entrepreneur, educationist and mentor. </em></strong><strong><em>A Stanford graduate in Electrical Engineering with an MBA from LUMS, Jazib is currently working as the Chief Operations Officer at Tintash, a Lahore-based software house developing web and mobile apps for clients around the world. He is also an adjunct faculty member at the Lahore University of Management Sciences. In this interview, Jazib talks about his time at Aitchison and the impact it has had on his life.</em></strong></p>
                        
                        <p class="question"><strong>When did you join Aitchison and what was it like back then? Were you a boarder or a day boy? What were you like as a student?</strong></p>
                        
                        <p align="justify">I joined Aitchison in class 3 in 1991 and stayed there till I graduated from A-levels in 2001. I was a day boy whose only interaction with the boarding house was the hot lunches offered when the working day was periodically extended.</p>
                        
                        <p align="justify">It is difficult to give a neutral opinion about myself. I definitely cared about school and felt I could connect with all the diverse groups around me. Probably not one of the most adventurous people around, so don&rsquo;t have any fun stories about bunking classes or being naughty, for better or for worse.</p>
                        
                        <p class="question"><strong>Has Aitchison influenced your journey? What kind of impact, if any, has it had on your life?</strong></p>
                        
                        <p align="justify">Most critically, Aitchison provided me with mentors and role models who inspired me to think big. This includes both faculty members as well as the many accomplished seniors and alums who remain a source of guidance till today.</p>
                        
                        <p align="justify">Secondly, Aitchison gave me identity. The brand name is recognized all over Pakistan and even around the world. No matter where I go, I am quickly able to pull together an impromptu Aitchison reunion. Countless times when I&rsquo;ve mentioned to people abroad that I&rsquo;m from Pakistan, they&rsquo;ve been able to guess right away that I&rsquo;m from a school that some of their friends have attended which is modeled on British boarding schools, has lush lawns and where all the alums bond in a manner that they haven&rsquo;t seen elsewhere.</p>
                        
                        <p align="justify">Finally, Aitchison taught me discipline and diligence that I haven&rsquo;t found the inspiration for anywhere else in the world. This encompasses everything from knowing when not to lean back in a chair to understanding when it&rsquo;s my turn to speak.</p>
                        
                        <p class="question"><strong>Any fond or interesting memories from your time at Aitchison?</strong></p>
                        
                        <p align="justify">Positive memories definitely outweigh any negative sentiments! General Assembly is a great memory. Don&rsquo;t think I&rsquo;ll get that feeling of being in a Roman Coliseum again. When we used to walk back to classes after the assembly, we always used to explore different pathways through the vast college and explore its nooks and crannies.</p>
                        
                        <p align="justify">I have soft spot for that stage in the Center of the Amphitheater. I associate it with our school plays and Founders Day events. Those were great character-building exercises.</p>
                        
                        <div class="row">
                        	<div class="col-lg-4 col-md-4 col-sm-12" style="padding-right:20px; line-height:1.6em;">
                            	<p align="justify">If I were to compile a series of Aitchison flashbacks, I would definitely include: cross-country runs, waiting for those yellow envelopes with result cards like Harry Potter waiting for his letter to Hogwarts, any photo session with Bhatti Sahib, the year we inhabited the Old Building, and finally, being thrown out of campus on our last day for the indiscipline of a few!</p>
                        	</div>
                            <div class="col-lg-8 col-md-8 col-sm-12">
