                            	<p align="justify">The calm and composed Vice Principal (Admin)/Bursar Principal had an air of humility and quiet authority that commanded respect.  He had an eagle eye for detail and a strong work ethic. He was a man of principle, loyalty, service and honour. He was a fine story-teller who never lost his wonderful sense of humour.</p>
                        
                        		<p align="justify">After a long and dedicated service to the College, Iftikhar resigned from the position as Vice Principal (Admin)/Bursar on 15th April, 2016. He was given a befitting farewell by College Principal Michael A. Thomson and the rest of the Aitchison family - an integral part of which he shall always remain. In order to honour his memory and to commemorate his service to the College, Principal Thomson has decided to name his old office in the Administration Block as ‘The Iftikhar Malik Room’.  His family and friends will be invited to preside over the unveiling of his plaque.</p>
