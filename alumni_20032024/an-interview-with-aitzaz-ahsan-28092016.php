                            <p class="question">What are you doing when not working?</p>
                            <p align="justify">I read a lot, mostly history, not law. Unfortunately, my capacity to read per day is largely exhausted by reading files, books, judgments and precedents. But I love reading history, particularly South Asian history. I&rsquo;ve written on that also. My book the &lsquo;Indus Saga&rsquo;, which was written during my jail terms under Zia-ul-Haq, has done almost 40 print outs by now. </p>
                            <p class="question">Are you in touch with your old batch mates?</p>
                            <p align="justify">We have a group we call ACOFA – Aitchison College Old Friends&rsquo; Association. My batch was '63 but from class of '61 to '65, we get together every month. One of us hosts dinner. I can&rsquo;t attend that often but when I go there it&rsquo;s about 15 - 16 old Aitchisonians of this period who get together once every month. Although we are a total group of 50 who try to come to dinners. </p>
                            <p class="question">Any advice for our current students?</p>
                            <p align="justify">Aitchisonians have to learn to be more caring. I find them very bright and brilliant but somewhat aloof, somewhat distanced from Pakistan and that is to my mind a structural flaw. But it’s born around the exclusivity that Aitchison offers and the total management of the life of a young boy from age 5 to age 18. Aitchison just embraces him, envelops him and virtually devours him. That is the only thing I’d say. </p>
