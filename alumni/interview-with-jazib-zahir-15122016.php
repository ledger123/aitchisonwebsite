                        </div>
                        <br />
                        <p class="question"><strong>Who was your favourite teacher in school? What did you like the most about him/her?</strong></p>
                        
                        <p align="justify">Too many! Kashmiri Sahib brought an incredible analytical approach to teaching something as subjective as English language. I&rsquo;d like to believe I carry his torch by bringing an analytical approach to teaching &lsquo;Business Communication&rsquo; at LUMS.</p>
                        
                        <p align="justify">Amir Hussain Sahib and Muhammad Tahir Sahib were influential teachers for me in Senior School. I liked how they never shied from sharing their opinions about the school and life in general. Tahir Sahib&rsquo;s passion for quizzes and competitions rubbed off on me.</p>
                        
                        <p align="justify">Have fond memories of Madam Shakira and Farhat Jabeen in Prep School as well as all my class teachers from Junior School.</p>
                        
                        
                        <p class="question"><strong>How do you define success?</strong></p>
                        
                        <p align="justify">Professional success comes from developing a specialization in life, a topic on which you are potentially a global authority.</p>
                        
                        <p align="justify">Personal success comes from knowing you wake up every day excited about what&rsquo;s coming ahead because you are surrounded by people and circumstances you care about.</p>
                        
                        <p align="justify">Ultimate success is finding a place for both in your life.</p>
                        
                        <p class="question"><strong>Which one achievement are you most proud of?</strong></p>
                        
                        <p align="justify">Coming back to Pakistan after studying abroad, and cultivating a career and lifestyle that leave me with no regrets.</p>
                        
                        <p class="question"><strong>What do you think distinguishes Aitchison College from other institutions?</strong></p>
                        
                        <p align="justify">The willingness to embrace history, culture and discipline while other institutions are moving away from it. I still feel the institution has an inherent aversion to becoming too commercial, something that plagues most private educational institutions. Merit continues to be of value there. You aren&rsquo;t special at Aitchison just because you have a recognizable last name.</p>
                        
                        <p class="question"><strong>What do you enjoy doing in your spare time?</strong></p>
                        
                        <p align="justify">Whatever little spare time I have, I enjoy letting it feel as unstructured as possible. Impromptu sports, reading and random discussions with family are things I look forward to. Living in Lahore also makes it possible for me to stay in touch with many friends, classmates, entrepreneurs and others so I enjoy meeting such people and re-connecting with them whenever they are in town or whenever there is an event that brings such like-minded people together.</p>
                        
                        <p class="question"><strong>Any advice for our current students?</strong></p>
                        
                        <p align="justify">The opportunities you have today are exponentially more than what I had. It also comes with exponentially more responsibility. It&rsquo;s common today for students your age to already being running businesses or solving problems around the world or attempting university level work. Do realize that the expectations for an Aitchisonian are very high in today&rsquo;s world and push yourself accordingly.</p>
                        
                        <p align="justify">Also realize that in addition to your performance on paper, it&rsquo;s important to develop social and practical skills. Employers and universities today are more excited about your portfolios of all the practical things you can do than your grades.</p>
                        
                        <p align="justify">And never miss an opportunity to pick the mind of an older, wiser person. Mentoring makes all the difference.</p>

                        
                        
                        
